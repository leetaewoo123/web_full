package hello;

public class Num {

	public static void main(String[] args) {
		int num = 10;
		int bNum = 0B1010;  // 2진수
		int oNum = 012;		// 8진수
		int hNum = 0xA;		// 16진수

		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(hNum);

		
	}

}
